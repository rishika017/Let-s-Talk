                        <h2 class="card-title">1. Your Wellness</h2>
                           <p>Old habits die hard, hence we bring to you a self help tool that will help you unfold a change at your own pace.

Our Wellness Program consists of 9 self help tools spanning on building life skills and healthy coping mechanisms around self, relationships and professional life without an involvement of a counselor or a therapist.

Each self help tool has been extensively replicated from the years of experience in counseling sessions and built around different clinically proven therapeutic techniques like cognitive behavioral therapy. All 9 self help tools are designed to take you through various stages in therapy - Self-probing, reflecting, developing insights, establishing goals, practicing new behavior and tracking progress.</p>
                       
                    <h2 class="card-title">2. Deal With Breakup</h2>
                     <p>It is hard to make sense of a loss when you don't have right questions. We take you on a journey that will help you reflect on your relationship, process your loss, develop a new sense of self and make you stronger.</p>

                   <h2 class="card-title">3. Dealing With Pre Marital Jitters</h2>
                     <p>A new beginning can be make you feel muddled with uncertainties. Marriage, a life-transition, can be stressful if we do not know what's coming or when we feel under prepared to take on a new role. Our Couple-therapy techniques will help you navigate a path with your partner to understand each other better and discover a bond leading to a fulfilling relationship.</p>

                  <h2 class="card-title">4. Improve Communication</h2>
                    <p>Whether its the beginning of a relationship or a decade of marriage, couples can get stuck with a communication pattern that doesn't help their relationship grow. This self help can help you identify your current pattern of communication and build healthier ways of expressing yourself.</p>

                  <h2 class="card-title">5.Parenting Skills</h2>
                    <p>A heavily demanding role where you re-organize your priorities, can lead you to feeling guilt, anger, and helplessness. Our module based on Parental coaching will help you learn new approach on becoming a better parent.</p>

                 <h2 class="card-title">6. Enhance Confidence</h2>
                   <p>Over the years, we may have let self-doubt shape our life-choices and experiences. Our self help tool will help you reflect on your unhelpful thinking patterns; and techniques based on cognitive behavior therapy will aid you developing and enhancing your self esteem.</p>
 
                <h2 class="card-title">7. Beat Addiction</h2>
                   <p>Any repetitive indulgence can lead to us feeling guilty or take a toll on our other areas of life- financial, relationship or work or simply our mental health. A self tool that replicates the model of de-addiction therapy can help you develop new habits and better coping skills to manage stress.</p>

               <h2 class="card-title">8. Overcome Anxiety</h2>
                 <p>Our worries can take a shape of physical symptoms such as rapid heartbeat while giving a presentation or fumbling words while being introduced to someone. This self-help will give you tools for a better self-management.</p>

              <h2 class="card-title">9. Tackle Peer Pressure</h2>
                <p>Unable to say no, or feeling worse after giving in to our peer group? This self help tool will take you through steps of overcoming the pressure, being more assertive and developing techniques to make a difficult conversation.</p>

              <h2 class="card-title">10. Balance Work & Life</h2>
               <p>At times we can feel overwhelmed with challenges at work, managing our both personal & professional life as well as dealing with surmounting pressures of today's environment. Our Work-Life Balance Self Help tool will help you manage stress and develop rituals & habits for a better balance.</p>
                                  
   <br><br>
                           <h5 class="card-title">We will be developing more tools to help you cope with different kinds of emotional pain and challenges that life can bring.

Our wellness program is most suitable for someone who doesn't feel ready yet to seek therapy and are uncomfortable navigating their concerns on their own. One can also understand how therapy will look like by enrolling on our program.

These tools act as support and we advise you not to consider it as a substitute for therapy if you still struggle to deal with root-cause concerns. Remember, in real life therapy a counselor will gauge into your wider concerns, with a broader scope to revisit your goals and share with you customised tools and perspective suitable to your life experiences and your needs.</h5>



