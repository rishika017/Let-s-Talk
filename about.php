          <h3 class="text-center py-5">What Is Mental Health?</h3>
<h5>Mental health includes our emotional, psychological, and social well-being. It affects how we think, feel, and act. It also helps determine how we handle stress, relate to others, and make choices. Mental health is important at every stage of life, from childhood and adolescence through adulthood.
</h5><br>
<p><h5>Over the course of your life, if you experience mental health problems, your thinking, mood, and behavior could be affected. Many factors contribute to mental health problems, including:
</h5></p>
<p>
- Biological factors, such as genes or brain chemistry</p>
<p>
- Life experiences, such as trauma or abuse</p>
<p>
- Family history of mental health problems</p>
<p>
Mental health problems are common but help is available. People with mental health problems can get better and many recover completely.</p>
