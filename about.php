<section class="my-5">
    <div class="container">
          <h3 class="text-center py-5">Early Warning Signs</h3>
<p><h5>Not sure if you or someone you know is living with mental health problems? Experiencing one or more of the following feelings or behaviors can be an early warning sign of a problem:
</h5></p>

<p>- Eating or sleeping too much or too little</p>
<p>- Pulling away from people and usual activities</p>
<p>- Having low or no energy</p>
<p>- Feeling numb or like nothing matters</p>
<p>- Having unexplained aches and pains</p>
<p>- Feeling helpless or hopeless</p>
<p>- Smoking, drinking, or using drugs more than usual</p>
<p>- Feeling unusually confused, forgetful, on edge, angry, upset, worried, or scared</p>
<p>- Yelling or fighting with family and friends</p>
<p>- Experiencing severe mood swings that cause problems in relationships</p>
<p>- Having persistent thoughts and memories you can't get out of your head</p>
<p>- Hearing voices or believing things that are not true</p>
<p>- Thinking of harming yourself or others</p>
<p>- Inability to perform daily tasks like taking care of your kids or getting to work or school</p>

