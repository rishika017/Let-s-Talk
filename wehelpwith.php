    <h2 align="center">1. Counseling for Relationships</h2>
      <p>Love is enough to sustain a bond between two people- This notion couldn’t be farther from the truth.
      <br>
      Often family bonds deteriorate, friends drift apart and romantic relationships reach a bitter end due to unresolved hurts, unmet needs, disappointments and unrealistic expectations that cause pain and build resentments.
      <br>
      Love is a necessary factor for any relationship be it family, friends or romantic partners but there are certain skills and often some healing that needs to take place to offer consistency, sense of safety and security, healthy communication and boundaries in our relationships. Some we learn with subsequent experiences and introspection, for some, we might need professional help to identify, heal and empower ourselves.
      <br>
      Therapy offers a space where healing, integration and holistic growth can take place for the broken relationships. It helps to explore and understand how unique, subjective elements of each individual personality might be creating friction in the relationship and how to repair the rupture caused due to it.</p> 
<br><div align="center">
<img width="500" src="\img\images (3).jpg" alt="">    
<br>
</div>
<br>
<h3>How Relationship Counselling Helps:</h3>
      <p>
        <ul>
         <li> Explore ideas and patterns of relationships</li>
         <li> Identify met and unmet needs</li>
         <li> Managing expectations</li>
         <li> Work on communication style</li>
         <li> Boundary setting</li>
         <li> Conflict resolution</li>
        </ul>
      </p>
<br>
    <h3>Therapy Approach:</h3>  
      <p>
        <ul>
          <li> Your counselor will spend the initial part of the therapy in building a rapport with you and giving you a safe, non-judgemental atmosphere to share your challenges.</li>
          <li> The counselor will understand the current concerns, explore the relationship in-depth, identify the areas of work, set goals for therapy.</li>
          <li> Gain an understanding of the patterns of communication, attachment styles, present triggers, unresolved conflicts, unhealed relational wounds and its impact.</li>
          <li> After receiving clarity on the length and breadth of the relationship concern, the Counselor will help to identify whether individual sessions will suffice or there is a need for couples counseling or family therapy as well to find a resolution.</li>
          <li> When do we need individual sessions- When individual personality factors like insecurity, anger outbursts, rigid beliefs, unrealistic expectations, inability to regulate emotions, possessiveness, controlling nature impact the interaction or when a previous trauma continues to trigger us in the present and leads to dysfunctional attachment patterns.</li>
          <li> When do we need couples counseling or family therapy- When two or more people’s communication patterns, belief systems, attachment styles, distress tolerance, needs, expectations, rigid and flexible, imbalance between intimacy and autonomy, boundaries lead to destructive patterns of codependency, enmeshment, toxic relationships that hurt one or all involved.</li>
          <li> The Counselor will help to chart a course of action and step by step take up each problem area of the relationship like controlling behaviour. personality clashes, lack of space, or autonomy, fair fighting, boundary setting and equip you with skills to resolve conflicts change perspectives, find a middle ground or part amicably.</li>
          <li> You may find noticeable changes after a few sessions where you would be able to identify triggers, deal with conflict effectively and are able to break the cycle of negative interactions.</li>
          <li> The aim of the counselor is to empower you in your journey to build healthy relationships with the people you love.</li>
        </ul>
      </p>

            <h4>Break Ups</h4>   
         <p>  Many times our love stories do not end in a “happily ever after”. Factors like communication gap, lack of quality time, mistrust, egos, long distance or other conflicts end up breaking relationships and our hearts. In such times, it is very important to understand that time heals all. We can use this time and invest in ourselves. </p>                       
  
            <h4>Lack of Trust in Relationships</h4>  
              <p>We have been together for 4 years now. Most of the times everything is great but the times where he is traveling for work or busy for days, thoughts of him being with another woman fill my head. He reassures me that he is committed to me but even after all this time together, I'm unable to get rid of these doubts. After all he DID have a PAST. I just don't know how to find sanity.</p>

            <h4>Losing The Spark</h4>  
          <p>“It's not like before anymore. We used to plan dates, slow down our day with a quick call to catch up, get upset with each other and spend more time making up for bad words said to each other. Now, we are only hurriedly rushing towards things that don't seem to bring us together at any point in the day.</p>

          <h4>Falling Out of Love</h4>
             <p>A successful marriage requires falling in love many times, always with the same person. Unmanaged conflicts and negative interactions can cause marital conflict. Communication, understanding and objectivity can turn a conflict into a learning experience for the couple and they can bring back the joy in their married life!</p>
  
          <h4>Build a Fulfilling Marriage</h4> 
            <p>A successful marriage requires falling in love many times, always with the same person. Unmanaged conflicts and negative interactions can cause marital conflict. Communication, understanding and objectivity can turn a conflict into a learning experience for the couple and they can bring back the joy in their married life!</p> 

          <h4>Family Bonds</h4>  
             <p>“The bond that links one’s true family is not one of blood, but of respect and joy in each others life." Family is a unique system that has its own styles of communication and behavior towards its members. Untying knots between family members, communicating and giving space to each other can bring back joy to a family.</p>

          <h4>Dating</h4>
             <p>Romantic relationships are a beautiful experience. However, life is not always about walking over petals of roses. The desire to be accepted and the possibility of rejection can lead to feelings of insecurity and trust issue. Better communication and building trust are the two pillars that will help make your relationships stronger.</p>                   

          <h4>Cheating in Relationship</h4>
             <p>“We have been together for 5 years and it was happening right under my nose and I didnt even realise. I trusted him with all my heart. Not just as a partner, but as a human being. How could he stoop so low to cheat and break my trust like this?”</p>

          <h4>Cheating in Marriage</h4>
              <p>Cheating in a marriage is a devastating and an emotionally painful experience. Many couples part ways and marriages get torn apart due to infidelity. It is hard to imagine that something like this could actually happen to us and first and foremost there is a feeling of shock.</p>

          <h4>Friendship</h4>
             <p>Friendships are an important part of our lives as we share so much with them. Sometimes due to time constrains, ego clashes, miscommunication & certain other factors our friendships tend to become sour. With a little trust, understanding and communication, such differences can be resolved and we can continue to cherish and celebrate friendship.</p>

           <h4>Couples Counseling</h4>
             <p>A lot of couples tend to seek therapy as a final resort or a last ditch attempt when nothing else seems to be working for them. According to a research by Gottman Institute, couples tend to wait a minimum of 6 years of being dissatisfied before they decide to seek help.</p>
